def strip_markdown_code_block(raw: str) -> str:
    """去除 AI 返回的 markdown 代码块标记（```json ... ```）

    因为各 AI 模型经常在 JSON 响应外层包裹 markdown 代码块，
    此函数统一处理剥离逻辑，避免在多个 service 中重复。
    支持多种变体：```json\\n...\\n```、```\\n...\\n```、```...``` 等。
    """
    text = raw.strip()
    # 匹配以 ``` 开头（可选语言标识），以 ``` 结尾的模式
    if text.startswith("```"):
        # 去掉首行（``` 或 ```json 等）
        first_newline = text.find("\n")
        if first_newline != -1:
            text = text[first_newline + 1:]
        else:
            # 全在一行的情况：```json{...}```
            text = text[3:]
        # 去掉尾部的 ```
        if text.endswith("```"):
            text = text[:-3]
        text = text.strip()
    return text
